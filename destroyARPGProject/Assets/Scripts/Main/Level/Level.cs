using UnityEngine;
using System.Collections;

public class Level {

	int _id;
	int _place;
	int _floor;
	int _power;

	public int _Id
	{
		get { return _id;}
		set {_id = value;}
	}
	public int _Place
	{
		get { return _place;}
		set {_place = value;}
	}
	public int _Floor
	{
		get { return _floor;}
		set {_floor = value;}
	}
	public int _Power
	{
		get { return _power;}
		set {_power = value;}
	}
}
